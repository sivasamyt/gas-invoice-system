            'price_per_liter' => 'sometimes|numeric|min:0',
            'description' => 'nullable|string'
        ]);
        $product->update($data);
        return response()->json($product);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
